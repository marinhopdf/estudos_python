a = input('Digite algo: ')
print('Teste para alfa-numérico: ', a.isalnum())
print('Teste para alfabeto: ', a.isalpha())
print('Teste para decimal: ', a.isdecimal())
print('Teste para caixa-alta: ', a.isupper())
print('Teste para espaço: ', a.isspace())
print('Teste para digito: ', a.isdigit())
print('Teste para ASCII: ', a.isascii())
print('Teste para imprimível: ', a.isprintable())
